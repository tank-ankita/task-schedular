// Class to represent a Developer in the Task Scheduler system
public class Developer {
    // Unique identifier for the developer
    int id;

    // Name of the developer
    String name;

    // Constructor to initialize a new Developer object with the given id and name
    public Developer(int id, String name) {
        this.id = id;
        this.name = name;
    }
}
